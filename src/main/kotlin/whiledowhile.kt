package com.dicoding.kotlin

fun main() {
//    While
    var counter = 1
    while (counter <= 7){
        println("Hello, World!")
        counter++
    }
    /*
       output :
           Hello, World!
           Hello, World!
           Hello, World!
           Hello, World!
           Hello, World!
           Hello, World!
           Hello, World!
    */

//    Do While
    do {
        println("Hello, World!")
        counter++
    } while (counter <= 7)
    /*
       output:
           Hello, World!
           Hello, World!
           Hello, World!
           Hello, World!
           Hello, World!
           Hello, World!
           Hello, World!
    */

}